int     ft_strncmp(char *s1, char *s2, unsigned int n)
{
    while (*s1 && *s2 && n > 0)
    {
        if (*s1 == *s2)
        {
            s1++;
            s2++;
            n--;
        }
        else
            return (*s1 - *s2);
    }
    if (n == 0)
        return (0);
    return (0);
}
